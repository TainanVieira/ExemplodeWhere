import java.util.Scanner;

public class ContagemRegressiva {
    public static void main(String[] args) {

        int valor = 0;

        Scanner ler = new Scanner(System.in);
        System.out.println("Digite um numero ");
        valor = ler.nextInt();
        while (valor > 0) {
            valor= valor-1;
            System.out.println(valor);

        }







    }
}
